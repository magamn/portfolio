var num = [1, 2, 3, 4, 30, 40];
var pow2 = 1;
var p = 1;
var count = 0;
for (var i = 0; i < num.length; ++i) {
    if (num[i] > pow2 && num[i] < p) {
        ++count;
    }
    pow2 = pow2 * 2;
    if (i > 0) {
        p *= i + 1;
    }
}
console.log(count);